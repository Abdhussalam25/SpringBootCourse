package finalvariablemethodandclass;

public class ClassWithFinalMethod {

	
	public int add(int num1,int num2) {
		
		return num1+num2;
	}
	
public final String add(String num1,String num2) {
		
		return num1+num2;
	}

public final String add(String num1,String num2,String num3) {
	
	return num1+num2+num3;
}
}


